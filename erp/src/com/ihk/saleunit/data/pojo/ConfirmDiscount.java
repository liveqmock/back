package com.ihk.saleunit.data.pojo;

import java.io.Serializable;
import java.util.Date;

public class ConfirmDiscount implements Serializable{
	private static final long serialVersionUID = 1L;
    
		private int id;
		private int unitDiscountId;
		private int projectDiscountId;
		private String isDeleted;
		private int createdId;
		private Date createdTime;
		private int modId;
		private Date modTime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
    
	public int getUnitDiscountId() {
		return unitDiscountId;
	}

	public void setUnitDiscountId(int unitDiscountId) {
		this.unitDiscountId = unitDiscountId;
	}
    
	public int getProjectDiscountId() {
		return projectDiscountId;
	}
	
	public void setProjectDiscountId(int projectDiscountId) {
		this.projectDiscountId = projectDiscountId;
	}
    
	public String getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(String isDeleted) {
		this.isDeleted = isDeleted;
	}
    
	public int getCreatedId() {
		return createdId;
	}

	public void setCreatedId(int createdId) {
		this.createdId = createdId;
	}
    
	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}
    
	public int getModId() {
		return modId;
	}

	public void setModId(int modId) {
		this.modId = modId;
	}
    
	public Date getModTime() {
		return modTime;
	}

	public void setModTime(Date modTime) {
		this.modTime = modTime;
	}
    
	
	public ConfirmDiscount(){};

	public ConfirmDiscount(    
		int id,
        		int unitDiscountId,
        		int projectDiscountId,
        		String isDeleted,
        		int createdId,
        		Date createdTime,
        		int modId,
        		Date modTime
        ) {
		super();  
		this.id = id;
		this.unitDiscountId = unitDiscountId;
		this.projectDiscountId = projectDiscountId;
		this.isDeleted = isDeleted;
		this.createdId = createdId;
		this.createdTime = createdTime;
		this.modId = modId;
		this.modTime = modTime;
	}
	public ConfirmDiscount(    
		int unitDiscountId,
        		int projectDiscountId,
        		String isDeleted,
        		int createdId,
        		Date createdTime,
        		int modId,
        		Date modTime
        ) {
		super();		
		this.unitDiscountId = unitDiscountId;
		this.projectDiscountId = projectDiscountId;
		this.isDeleted = isDeleted;
		this.createdId = createdId;
		this.createdTime = createdTime;
		this.modId = modId;
		this.modTime = modTime;
	}
}
