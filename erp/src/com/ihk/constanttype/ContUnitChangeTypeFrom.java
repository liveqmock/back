package com.ihk.constanttype;

import java.util.ArrayList;
import java.util.List;

/**
 * 
 * 判断单元来自哪里
 * @author dtc
 *
 */
public class ContUnitChangeTypeFrom {
	
	/**
	 * 预约
	 */
	public final static String APPOINT = "appoint"; //预约
	
	/**
	 * 认购
	 */
	public final static String CONFIRM = "confirm"; //认购
	
	/**
	 * 合同(已签约)
	 */
	public final static String CONTRACT = "contract"; //合同(已签约)
	
	/**
	 * 返回typeFrom list
	 * @return
	 */
	public static List<String> typeFromList(){
		
		List<String> retList = new ArrayList<String>();
		
		retList.add(APPOINT);
		retList.add(CONFIRM);
		retList.add(CONTRACT);
		
		return retList;
	}

}
