package com.ihk.utils.common.unit;

import java.io.Serializable;

/**
 * 包含unitId,buildId,areaId,propertyProjectId,companyProjectId的pojo
 * @author dtc
 * 2013-4-25,上午10:39:45
 */
public class CommonUnitPojo implements Serializable{
	
	private static final long serialVersionUID = 6587904468100964208L;

	/**
	 * 单元id
	 */
	private int unitId;
	
	/**
	 * 楼栋id
	 */
	private int buildId;
	
	/**
	 * 分区id
	 */
	private int areaId;
	
	/**
	 * 楼盘项目id
	 */
	private int propertyProjectId;
	
	/**
	 * 公司项目id
	 */
	private int companyProjectId;

	public int getUnitId() {
		return unitId;
	}

	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}

	public int getBuildId() {
		return buildId;
	}

	public void setBuildId(int buildId) {
		this.buildId = buildId;
	}

	public int getAreaId() {
		return areaId;
	}

	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}

	public int getPropertyProjectId() {
		return propertyProjectId;
	}

	public void setPropertyProjectId(int propertyProjectId) {
		this.propertyProjectId = propertyProjectId;
	}

	public int getCompanyProjectId() {
		return companyProjectId;
	}

	public void setCompanyProjectId(int companyProjectId) {
		this.companyProjectId = companyProjectId;
	}
	
	public CommonUnitPojo() {
	}

	public CommonUnitPojo(int unitId, int buildId, int areaId,
			int propertyProjectId, int companyProjectId) {
		super();
		this.unitId = unitId;
		this.buildId = buildId;
		this.areaId = areaId;
		this.propertyProjectId = propertyProjectId;
		this.companyProjectId = companyProjectId;
	}
	
}
