package com.ihk.utils.saleunitnew;

/**
 * 数据分组bean
 * @author dtc
 * 2012-9-29
 */
public class GroupDataLeftBean {
	
	private int id;
	
	private String name;
	
	private String groupType; //分组类型,用于请求参数的判断是否选择了该字段进行分组
	
	public void setGroupType(String groupType) {
		this.groupType = groupType;
	}
	
	public String getGroupType() {
		return groupType;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

}
