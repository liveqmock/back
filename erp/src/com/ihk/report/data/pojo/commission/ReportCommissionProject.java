package com.ihk.report.data.pojo.commission;

import java.util.Date;
import java.math.BigDecimal;
import java.io.Serializable;

/**
 * 佣金：项目
 * @author 
 *
 */
public class ReportCommissionProject implements Serializable{
	private static final long serialVersionUID = 1L;


	private int projectId;	//项目id
	private String projectName;	//项目名称
	private BigDecimal paymentAmount;	//已收佣金
	private BigDecimal notgetAmount;	//未收佣金


	/**
	 * 项目id
	 * @return
	 */
	public int getProjectId() {
		return projectId;
	}
	/**
	 * 项目id
	 * @param projectId
	 */
	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}

	/**
	 * 项目名称
	 * @return
	 */
	public String getProjectName() {
		return projectName;
	}
	/**
	 * 项目名称
	 * @param projectName
	 */
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	/**
	 * 已收佣金
	 * @return
	 */
	public BigDecimal getPaymentAmount() {
		return paymentAmount;
	}
	/**
	 * 已收佣金
	 * @param paymentAmount
	 */
	public void setPaymentAmount(BigDecimal paymentAmount) {
		this.paymentAmount = paymentAmount;
	}

	/**
	 * 未收佣金
	 * @return
	 */
	public BigDecimal getNotgetAmount() {
		return notgetAmount;
	}
	/**
	 * 未收佣金
	 * @param notgetAmount
	 */
	public void setNotgetAmount(BigDecimal notgetAmount) {
		this.notgetAmount = notgetAmount;
	}	
    
}
