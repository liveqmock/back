package com.ihk.customer.data.pojo;

import java.io.Serializable;

public class IdcardLocation implements Serializable {

	private static final long serialVersionUID = 5634655031162699616L;

	private int id;
	private int identityId;
	private String areaProvince;
	private String areaCity;
	private String areaRegion;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getIdentityId() {
		return identityId;
	}

	public void setIdentityId(int identityId) {
		this.identityId = identityId;
	}

	public String getAreaProvince() {
		return areaProvince;
	}

	public void setAreaProvince(String areaProvince) {
		this.areaProvince = areaProvince;
	}

	public String getAreaCity() {
		return areaCity;
	}

	public void setAreaCity(String areaCity) {
		this.areaCity = areaCity;
	}

	public String getAreaRegion() {
		return areaRegion;
	}

	public void setAreaRegion(String areaRegion) {
		this.areaRegion = areaRegion;
	}

	public IdcardLocation() {
	};

	public IdcardLocation(int id, int identityId, String areaProvince,
			String areaCity, String areaRegion) {
		super();
		this.id = id;
		this.identityId = identityId;
		this.areaProvince = areaProvince;
		this.areaCity = areaCity;
		this.areaRegion = areaRegion;
	}

	public IdcardLocation(int identityId, String areaProvince, String areaCity,
			String areaRegion) {
		super();
		this.identityId = identityId;
		this.areaProvince = areaProvince;
		this.areaCity = areaCity;
		this.areaRegion = areaRegion;
	}
}
